package Sep23;

public class CalculatorMethods {

    public int sum(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public int multi(int a, int b){
        return a * b;
    }

    public double div(double a, double b){
        return a / b;
    }

}
