public class Practice {
    public static void main(String[] args) {

       int num = 5;
       int result;

       for(int i = 1 ; i <= 5 ; i++ ){
           // 5 =  5 x 1
           // 10 = 5 x 2
           // 15 = 5 x 3
           // 20 = 5 x 4
           // 25 = 5 x 5
            result = num * i;

            // 5   x   1   =   5
           // num  x   i   =   result
           System.out.println(num + " x " + i + " = " + result);
       }


    }
}
